package fundamentos;

public class VariaveisEConstantes {
    static void main() {

        // 1. Declarar uma variável
        int idade;

        // 2. Inicializar a variável
        idade = 3;

        // Aqui temos uma constante (final)
        final String nome = "João da Silva";

        System.out.println(nome);
        System.out.println(idade);

        int a = 4;
        int b = 7;
        int resultado = a + b;

        System.out.println(resultado);
    }
}
