package operadores;

public class Atribuicao {

    static void main() {

        int a = 7;
        System.out.println(a);

        a += 3;
        System.out.println(a);

        a -= 2;
        System.out.println(a);

        a *= 3;
        System.out.println(a);

        a /= 2;
        System.out.println(a);

        a %= 3;
        System.out.println(a);
    }
}
