package operadores;

@SuppressWarnings("all")
public class Relacionais {

    static void main() {

        int a = 7;
        int b = 3;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == 7);
        System.out.println(a == b);
        System.out.println(a != 7);
        System.out.println(a != b);
        System.out.println(7 == '7');
    }
}
